    private void loadMedicines() {

    medicines = medicineDAO.getAllMedicines();

    cmbMedicine.removeAllItems();

    cmbMedicine.addItem("Select Medicine");

    for (Medicine medicine : medicines) {

        cmbMedicine.addItem(medicine.getMedicineName());
    }
}
    private void showMedicineDetails() {

    int selectedIndex = cmbMedicine.getSelectedIndex();

    // Index 0 is "Select Medicine"
    if (selectedIndex <= 0) {

        txtAvailable.setText("");
        txtPrice.setText("");

        return;
    }

    Medicine selectedMedicine = medicines.get(selectedIndex - 1);

    txtAvailable.setText(
            String.valueOf(selectedMedicine.getQuantity())
    );

    txtPrice.setText(
            String.valueOf(selectedMedicine.getPrice())
    );
}

