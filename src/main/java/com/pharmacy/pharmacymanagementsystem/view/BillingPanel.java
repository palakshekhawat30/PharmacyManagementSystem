    private void btnAddToBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToBillActionPerformed
        // TODO add your handling code here:
        int selectedIndex = cmbMedicine.getSelectedIndex();

    if (selectedIndex <= 0) {

        JOptionPane.showMessageDialog(
                this,
                "Please select a medicine!"
        );

        return;
    }

    String quantityText = txtQuantity.getText().trim();

    if (quantityText.isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Please enter quantity!"
        );

        return;
    }

    int quantity;

    try {

        quantity = Integer.parseInt(quantityText);

    } catch (NumberFormatException e) {

        JOptionPane.showMessageDialog(
                this,
                "Please enter a valid quantity!"
        );

        return;
    }

    Medicine selectedMedicine =
            medicines.get(selectedIndex - 1);

    int availableQuantity =
            selectedMedicine.getQuantity();

    double price =
            selectedMedicine.getPrice();

    if (quantity <= 0) {

        JOptionPane.showMessageDialog(
                this,
                "Quantity must be greater than zero!"
        );

        return;
    }

    if (quantity > availableQuantity) {

        JOptionPane.showMessageDialog(
                this,
                "Insufficient stock! Available quantity: "
                + availableQuantity
        );

        return;
    }

    /*
     * Create SaleItem
     */
    SaleItem saleItem = new SaleItem(
            selectedMedicine.getId(),
            selectedMedicine.getMedicineName(),
            quantity,
            price
    );

    /*
     * Add SaleItem to list
     */
    billItems.add(saleItem);

    /*
     * Add item to JTable
     */
    DefaultTableModel model =
            (DefaultTableModel) jTableBill.getModel();

    model.addRow(new Object[]{

        selectedMedicine.getMedicineName(),
        quantity,
        price,
        saleItem.getTotal()

    });

    /*
     * Calculate Grand Total
     */
    calculateGrandTotal();

    txtQuantity.setText("");
    }//GEN-LAST:event_btnAddToBillActionPerformed

    private void btnGenerateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillActionPerformed
        // TODO add your handling code here:
     String customerName =
            txtCustomerName.getText().trim();

    if (customerName.isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Please enter customer name!"
        );

        return;
    }

    if (billItems.isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Please add at least one medicine to the bill!"
        );

        return;
    }

    double grandTotal = 0;

    for (SaleItem item : billItems) {

        grandTotal += item.getTotal();
    }

    Sale sale = new Sale(
            customerName,
            grandTotal
    );

    boolean success =
            billingDAO.completeSale(
                    sale,
                    billItems
            );

    if (success) {

        JOptionPane.showMessageDialog(
                this,
                "Bill generated successfully!"
        );

        clearBilling();

        /*
         * Reload medicines because stock has changed
         */
        loadMedicines();

    } else {

        JOptionPane.showMessageDialog(
                this,
                "Failed to generate bill!"
        );
    }
    }//GEN-LAST:event_btnGenerateBillActionPerformed

